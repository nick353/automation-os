const artifactScanMaxDepth = 3;
const artifactScanMaxFiles = 200;
const artifactScanMaxEntries = 1000;

export type ResumeContractLocator = {
  label: string;
  path: string;
  kind: string;
  exists: boolean;
  note: string;
};

export type ResumeContractProject = {
  name: string;
  cwd: string;
  authorityFiles: ResumeContractLocator[];
  latestArtifact: ResumeContractLocator | null;
  memoryHints: ResumeContractLocator[];
  locators: ResumeContractLocator[];
};

export type ResumeContract = {
  readFirst: ResumeContractLocator[];
  projects: ResumeContractProject[];
  authorityFiles: ResumeContractLocator[];
  latestArtifact: ResumeContractLocator | null;
  memoryHints: ResumeContractLocator[];
  resumeRule: string;
  generatedAt: string;
};

export type ResumeContractOptions = {
  vaultPath?: string;
  startHereSubdir?: string;
  contractPath?: string;
  codexMemoryFile?: string;
  generatedAt?: string;
};

type FileScanBudget = {
  files: string[];
  entriesVisited: number;
  maxDepth: number;
  maxFiles: number;
  maxEntries: number;
};

export function resolveResumeContractPath(input?: string): string {
  return resolve(input ?? process.env.AUTOMATION_OS_RESUME_CONTRACT_PATH ?? defaultResumeContractPath);
}

export function buildResumeContract(options: ResumeContractOptions = {}): ResumeContract {
  const generatedAt = options.generatedAt ?? new Date().toISOString();
  const vaultPath = resolve(options.vaultPath ?? process.env.AUTOMATION_OS_OBSIDIAN_VAULT ?? defaultVaultPath);
  const startHereDir = join(vaultPath, options.startHereSubdir ?? defaultStartHereSubdir);
  const contractPath = resolveResumeContractPath(options.contractPath);
  const projectHandoffIndex = join(startHereDir, "Project Handoff Index.md");
  const resumeCurrentWork = join(startHereDir, "Resume Current Work.md");
  const projectMemoryMap = join(startHereDir, "Project Memory Map.md");
  const resumeContractNote = join(startHereDir, "Resume Contract.md");
  const userAgentsFile = join(homedir(), "AGENTS.md");
  const codexAgentsFile = join(homedir(), ".codex", "AGENTS.md");
  const globalAuthorityFiles = [
    locator("User AGENTS", userAgentsFile, "authority-file", "User-level Codex instructions. Read before asking the user to restate context."),
    locator("Codex AGENTS", codexAgentsFile, "authority-file", "Codex user-level instructions and resume guardrails.")
  ];
  const memoryFile = resolve(options.codexMemoryFile ?? process.env.AUTOMATION_OS_CODEX_MEMORY_FILE ?? join(homedir(), ".codex", "memories", "MEMORY.md"));
  const memoryHints = readMemoryHints(memoryFile);
  const projectRoots = collectProjectRoots({ projectHandoffIndex, memoryHints });
  const projects = projectRoots.map((cwd) => buildProjectContract(cwd, { projectHandoffIndex, resumeCurrentWork, projectMemoryMap, memoryHints }));
  const authorityFiles = uniqueLocators([...globalAuthorityFiles, ...projects.flatMap((project) => project.authorityFiles)]);
  const latestArtifact = selectLatestLocator(projects.map((project) => project.latestArtifact).filter((locator): locator is ResumeContractLocator => Boolean(locator)));

  return {
    readFirst: [
      locator("Resume contract JSON", contractPath, "resume-contract-json", "Machine-readable handoff contract generated by Automation OS."),
      locator("User AGENTS", userAgentsFile, "authority-file", "User-level Codex instructions. Read before asking the user to restate context."),
      locator("Codex AGENTS", codexAgentsFile, "authority-file", "Codex user-level instructions and resume guardrails."),
      locator("Project Handoff Index", projectHandoffIndex, "obsidian-handoff-index", "Existing stop-hook locator. Read only; Automation OS export does not overwrite it."),
      locator("Resume Current Work", resumeCurrentWork, "obsidian-resume-brief", "Generated short resume brief for the latest Automation OS context."),
      locator("Project Memory Map", projectMemoryMap, "obsidian-project-memory-map", "Generated project/session locator map."),
      locator("Resume Contract note", resumeContractNote, "obsidian-resume-contract", "Human-readable view of this contract.")
    ],
    projects,
    authorityFiles,
    latestArtifact,
    memoryHints: memoryHints.slice(0, 12),
    resumeRule,
    generatedAt
  };
}

export function writeResumeContract(contract: ResumeContract, contractPath = resolveResumeContractPath()): string {
  const path = resolveResumeContractPath(contractPath);
  mkdirSync(dirname(path), { recursive: true });
  const tmpPath = `${path}.${process.pid}.${Date.now()}.tmp`;
  writeFileSync(tmpPath, `${JSON.stringify(contract, null, 2)}\n`);
  renameSync(tmpPath, path);
  return path;
}

export function readResumeContractFile(contractPath = resolveResumeContractPath()): ResumeContract | null {
  const path = resolveResumeContractPath(contractPath);
  if (!existsSync(path)) return null;
  try {
    return JSON.parse(readFileSync(path, "utf8")) as ResumeContract;
  } catch {
    return null;
  }
}

export function getResumeContract(options: ResumeContractOptions = {}): ResumeContract {
  return buildResumeContract(options);
}

