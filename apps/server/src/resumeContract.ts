function collectProjectRoots(input: { projectHandoffIndex: string; memoryHints: ResumeContractLocator[] }): string[] {
  const roots = new Set<string>();
  addProjectRoot(roots, homedir());
  addProjectRoot(roots, join(homedir(), ".codex"));
  addProjectRoot(roots, process.cwd());
  addProjectRoot(roots, "/Users/nichikatanaka/Documents/Codex/automation-os");
  for (const automation of getCodexCapabilities().capabilities.automations) {
    addProjectRoot(roots, automation.path.endsWith("automation.toml") ? dirname(expandHome(automation.path)) : expandHome(automation.path));
  }
  for (const hint of input.memoryHints) addProjectRoot(roots, hint.path);
  if (existsSync(input.projectHandoffIndex)) {
    for (const path of extractAbsolutePaths(safeReadText(input.projectHandoffIndex))) addProjectRoot(roots, path);
  }
  return Array.from(roots).slice(0, 24);
}

function buildProjectContract(
  cwd: string,
  input: {
    projectHandoffIndex: string;
    resumeCurrentWork: string;
    projectMemoryMap: string;
    memoryHints: ResumeContractLocator[];
  }
): ResumeContractProject {
  const authorityFiles = [
    "STATE.md",
    "AGENTS.md",
    "automation.toml",
    "SKILL.md",
    join("docs", "current-run-contract.md"),
    join("docs", "CURRENT.md")
  ].map((entry) => locator(entry, join(cwd, entry), "authority-file", "Project-owned source of truth or resume boundary."))
    .filter((entry) => entry.exists);
  const latestArtifact = findLatestArtifact(cwd);
  const projectHints = input.memoryHints.filter((hint) => pathsMayReferToSameProject(cwd, hint.path)).slice(0, 4);
  return {
    name: basename(cwd) || cwd,
    cwd,
    authorityFiles,
    latestArtifact,
    memoryHints: projectHints,
    locators: [
      locator("Project Handoff Index", input.projectHandoffIndex, "obsidian-handoff-index", "Existing generated stop-hook locator."),
      locator("Resume Current Work", input.resumeCurrentWork, "obsidian-resume-brief", "Generated Automation OS resume brief."),
      locator("Project Memory Map", input.projectMemoryMap, "obsidian-project-memory-map", "Generated project/session locator map.")
    ].filter((entry) => entry.exists)
  };
}

function findLatestArtifact(cwd: string): ResumeContractLocator | null {
  const candidates = [join(cwd, "artifacts"), join(cwd, "data", "artifacts")].filter((path) => existsSync(path));
  const budget = createFileScanBudget();
  for (const path of candidates) {
    listFilesBounded(path, 0, budget);
    if (isFileScanBudgetExhausted(budget)) break;
  }
  const files = budget.files;
  if (!files.length) return null;
  const latest = files
    .map((path) => ({ path, mtime: safeMtime(path) }))
    .sort((a, b) => b.mtime - a.mtime)[0];
  return locator("Latest artifact", latest.path, "artifact", "Most recently modified local artifact under the project.");
}

function createFileScanBudget(): FileScanBudget {
  return {
    files: [],
    entriesVisited: 0,
    maxDepth: artifactScanMaxDepth,
    maxFiles: artifactScanMaxFiles,
    maxEntries: artifactScanMaxEntries
  };
}

function isFileScanBudgetExhausted(budget: FileScanBudget): boolean {
  return budget.files.length >= budget.maxFiles || budget.entriesVisited >= budget.maxEntries;
}

function listFilesBounded(dir: string, depth: number, budget = createFileScanBudget()): string[] {
  if (depth > budget.maxDepth || isFileScanBudgetExhausted(budget)) return budget.files;
  let entries: string[];
  try {
    entries = readdirSync(dir);
  } catch {
    return budget.files;
  }
  for (const entry of entries) {
    if (entry.startsWith(".")) continue;
    if (isFileScanBudgetExhausted(budget)) break;
    budget.entriesVisited += 1;
    const path = join(dir, entry);
    try {
      const stat = lstatSync(path);
      if (stat.isDirectory()) {
        listFilesBounded(path, depth + 1, budget);
      } else if (stat.isFile()) {
        budget.files.push(path);
      }
    } catch {
      continue;
    }
  }
  return budget.files;
}

function readMemoryHints(memoryFile: string): ResumeContractLocator[] {
  if (!existsSync(memoryFile)) return [];
  const hints: ResumeContractLocator[] = [];
  for (const line of safeReadText(memoryFile).split("\n").slice(0, 2500)) {
    for (const path of extractMemoryPaths(line)) {
      hints.push(locator("Codex memory hint", path, "memory-hint", shortSnippet(line.replace(/^\s*[-#]+\s*/, ""), 180)));
    }
  }
  return uniqueLocators(hints).slice(0, 80);
}

function extractMemoryPaths(line: string): string[] {
  const paths: string[] = [];
  for (const match of line.matchAll(/scope:\s*`([^`]+)`/g)) paths.push(match[1].trim());
  for (const match of line.matchAll(/\bcwd(?:_family)?=([^;\),\n]+)/g)) {
    const raw = match[1].trim().replace(/^`|`$/g, "");
    paths.push(...raw.split(/\s+and\s+|,\s*/).map((entry) => entry.trim()));
  }
  return paths.map((path) => path.replace(/^`|`$/g, "")).filter((path) => path.startsWith("/") || path.startsWith("~/"));
}

function extractAbsolutePaths(markdown: string): string[] {
  const paths = new Set<string>();
  for (const match of markdown.matchAll(/(?:`|^|\s)(\/Users\/nichikatanaka\/[^\s`),;]+)/g)) {
    paths.add(match[1].replace(/[.:]+$/, ""));
  }
  return Array.from(paths);
}

function addProjectRoot(roots: Set<string>, inputPath: string): void {
  const normalized = normalizePath(inputPath);
  if (!normalized) return;
  const projectRoot = trimToProjectRoot(normalized);
  roots.add(projectRoot);
}

function trimToProjectRoot(path: string): string {
  const artifactIndex = path.split("/").findIndex((part) => part === "artifacts");
  if (artifactIndex > 0) return path.split("/").slice(0, artifactIndex).join("/");
  for (const marker of ["STATE.md", "AGENTS.md", "automation.toml", "SKILL.md"]) {
    if (path.endsWith(`/${marker}`)) return dirname(path);
  }
  return path;
}

function locator(label: string, path: string, kind: string, note: string): ResumeContractLocator {
  const resolved = normalizePath(path) ?? path;
  return {
    label,
    path: resolved,
    kind,
    exists: existsSync(resolved),
    note
  };
}

function uniqueLocators(locators: ResumeContractLocator[]): ResumeContractLocator[] {
  const seen = new Set<string>();
  return locators.filter((locator) => {
    const key = `${locator.kind}\n${locator.path}\n${locator.note}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function selectLatestLocator(locators: ResumeContractLocator[]): ResumeContractLocator | null {
  if (!locators.length) return null;
  return locators.sort((a, b) => safeMtime(b.path) - safeMtime(a.path))[0];
}

function pathsMayReferToSameProject(left: string, right: string): boolean {
  const normalizedLeft = normalizePath(left);
  const normalizedRight = normalizePath(right);
  if (!normalizedLeft || !normalizedRight) return left === right;
  return isSameOrInside(normalizedLeft, normalizedRight) || isSameOrInside(normalizedRight, normalizedLeft);
}

function isSameOrInside(path: string, root: string): boolean {
  const rel = relative(root, path);
  return rel === "" || (!rel.startsWith("..") && !isAbsolute(rel));
}

function normalizePath(path: string): string | undefined {
  const trimmed = path.trim().replace(/^`|`$/g, "");
  if (!trimmed) return undefined;
  return resolve(isAbsolute(expandHome(trimmed)) ? expandHome(trimmed) : join(process.cwd(), expandHome(trimmed)));
}

function expandHome(path: string): string {
  if (!path.startsWith("~/")) return path;
  return join(process.env.AUTOMATION_OS_CAPABILITIES_HOME ?? homedir(), path.slice(2));
}

function safeMtime(path: string): number {
  try {
    return statSync(path).mtimeMs;
  } catch {
    return 0;
  }
}

function safeReadText(path: string): string {
  try {
    return readFileSync(path, "utf8");
  } catch {
    return "";
  }
}

function shortSnippet(value: string, max: number): string {
  const compact = value.replace(/\s+/g, " ").trim();
  return compact.length > max ? `${compact.slice(0, max - 3)}...` : compact;
}
